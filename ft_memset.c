#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*a;
	size_t			i;

	i = 0;
	a = (unsigned char *)b;
	while (i < len - 1)
	{
		*a = (unsigned char)c;
		i++;
		a++;
	}
	return (b);
}
