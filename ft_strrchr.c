#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	while (*s)
	{
		s++;
	}
	if (c == '\0')
	{
		return ((char *)s);
	}
	while (*s-- >= 0)
	{
		if (*s == c)
		{
			return ((char *)s);
		}
	}
	return ((char *) NULL);
}
