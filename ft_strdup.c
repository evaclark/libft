#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	len;
	char	*ptr;

	len = ft_strlen(s1) + 1;
	ptr = malloc(sizeof(*s1) * len);
	ptr = ft_memcpy(ptr, s1, len);
	return (ptr);
}
